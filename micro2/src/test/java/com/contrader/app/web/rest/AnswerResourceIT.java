package com.contrader.app.web.rest;

import com.contrader.app.Micro2App;
import com.contrader.app.domain.Answer;
import com.contrader.app.domain.Quest;
import com.contrader.app.repository.AnswerRepository;
import com.contrader.app.service.AnswerService;
import com.contrader.app.service.dto.AnswerDTO;
import com.contrader.app.service.mapper.AnswerMapper;
import com.contrader.app.web.rest.errors.ExceptionTranslator;

import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.mockito.MockitoAnnotations;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.context.SpringBootTest;
import org.springframework.data.web.PageableHandlerMethodArgumentResolver;
import org.springframework.http.MediaType;
import org.springframework.http.converter.json.MappingJackson2HttpMessageConverter;
